class Pessoa
    def initialize(nome, sexo, idade, vegetariana)
        @nome = nome
        @sexo = sexo
        @idade = idade
        @vegetariana = vegetariana
    end

    def nome
        @nome
    end

    def sexo
        @sexo
    end
    def idade
        @idade
    end
    def vegetariana
        @vegetariana
    end
end